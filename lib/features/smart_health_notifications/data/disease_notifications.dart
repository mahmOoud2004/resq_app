  /// Returns a random tip for a specific disease without repeating the exact last tip.
  static Future<String> getRandomTip(String disease) async {
    final tips = diseaseTips[disease];
    if (tips == null || tips.isEmpty) return "نتمنى لك يوماً صحياً ومشرقاً!";

    final prefs = await SharedPreferences.getInstance();
    final String lastTipKey = 'last_tip_$disease';
    final int lastTipIndex = prefs.getInt(lastTipKey) ?? -1;

    final random = Random();
    int newIndex;
    
    if (tips.length == 1) {
      newIndex = 0;
    } else {
      do {
        newIndex = random.nextInt(tips.length);
      } while (newIndex == lastTipIndex);
    }

    await prefs.setInt(lastTipKey, newIndex);
    return tips[newIndex];
  }
